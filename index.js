      const message = await bot.sendMessage(chatId, `‚ÑπÔ∏è ${userMention}, el bot ya est√° ${isActivation ? 'activo' : 'inactivo'} en ${group.name}.${adminMessage}`, { message_thread_id: threadId, parse_mode: 'Markdown' });
